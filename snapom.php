class snapom {
	private $filename;
	private $maxsize;
	private $input;
	private $pack;

	public function __construct () {
		$this->filename = "snapom-data"; // FILE OF STORAGE
		$this->maxsize = pow(10,8);
		$this->input = array();
		$this->pack = array();
		$this->firewall();
	}

	private function firewall () {
		///// PROTECT GET AND POST /////
		foreach ($_GET as $name=>$value) {
			$this->input["get_$name"] = $this->filter($value); }
		foreach ($_POST as $name=>$value) {
			$this->input["post_$name"] = $this->filter($value); }
	}

	public function filter ($value) {
		///// SIMPLE FILTER /////
		return $value;
		return trim(htmlentities($value));
	}

	public function request () {
		///// EXTERNAL REQUEST /////
		if(isset($this->input['get_action'])) {
			$action = $this->input['get_action'];
			if($action == "script") {
				header('Content-Type: text/javascript');
				echo $this->pack[0];
			} else if($action == "style") {
				header('Content-Type: text/css');
				echo $this->pack[1];
			} else if($action == "upload") {
				$name = $_POST['name'];
				$data = $_POST['data'];
				if((strlen($data)/2) < $this->maxsize) {
					$this->file_set_data($this->filename, $name."\n".$data);
					echo "The file is saved. You can download it <a href='snapom.php?action=download'>here</a>.";
				} else {
					header("HTTP/1.1 418 OK");
				}
			} else if($action == "download") {
				$brut = $this->file_get_data($this->filename);
				$start = strpos($brut, "\n");
				$name = substr($brut, 0, $start);
				$data = $this->hexa_decode(substr($brut, $start+1));
				header("Content-Type: application/force-download; name=\"".$name."\"");
				header("Content-Length: ".strlen($data));
				header("Content-Disposition: attachment; filename=\"".$name."\"");
				echo $data;
			}
			exit;
		}
	}

	public function downLink () {
		if(file_exists($this->filename)) {
			return "You can <a href='snapom.php?action=download'>download</a> the latest version.";
		} else {
			return "No file is available. You can upload.";
		}
	}

	public function loadPack ($packScript, $packStyle) {
		///// LOAD PACK /////
		$this->pack[0] = $packScript;
		$this->pack[1] = $packStyle;
		return 1;
	}

	private function file_get_data ($path) {
		///// RETURN DATA OF FILE /////
		if(file_exists($path)) {
			return (filesize($path) > 0 ? fread(fopen($path,'rb'), filesize($path)) : "");
		} else { return ""; }
	}

	private function file_set_data ($path, $data) {
		///// WRITE DATA IN FILE /////
		$handle = fopen($path,'wb');
		ftruncate($handle,0);
		fwrite($handle,$data);
		fclose($handle);
		return 1;
	}

	/***** TOOLS *****/

	public function hexa_encode ($h) {
		$a = "";
		for($e=0;$e<strlen($h);$e++){
			$x=strtoupper(dechex(ord($h[$e])));$a.=(strlen($x)<2?"0".$x:$x);}
		return $a; }
	public function hexa_decode ($h) {
		$a = "";
		for($e=0;$e<strlen($h);$e+=2){
			$x=hexdec(substr($h,$e,2));$a.=chr($x); }
		return $a; }
}

